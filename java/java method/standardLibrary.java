
public class standardLibrary {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(18)); 
        System.out.println(Math.sqrt(25));//Math.sqrt return the double value.Thats why ans is 5.0
        
        System.out.println(Math.floor(5.3));
        System.out.println(Math.floor(9.4));

        System.out.println(Math.ceil(5.3));
        System.out.println(Math.ceil(9.4));
    }
    
}
/*1. Math.sqrt is a standard library for that reason we need not to creating object 
  2. floor means the greatest integer that is less than (or may be equal) x or 5.3 . so the ans is 5.0
  3. ceil means the least integer that is greater than (or may be equal )x or 5.3  . so the ans is 6.0
*/
