#include<stdio.h>
int main()
{
    char ch='A';//character type data declaration process.

    printf("%c\n",ch);//the output is A when we use %c format specifier for character type data.

    printf("%d\n",ch);/* when we use %d format specifier for character type data then the output is 65.this is the ASCII
    value of the character A.The ASCII value is integer type number.*/
    // character type data always have to be single character.
    return 0;
}
